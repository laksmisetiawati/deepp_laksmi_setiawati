CREATE TABLE customers (
  customer_id string(255),
  customer_name varchar(255),
  gender varchar(255),
  age int(11),
  home_address text,
  zip_code varchar(255),
  city varchar(255),
  state varchar(255),
  country varchar(255),
  birth_year int(11),
  generation varchar (255),
  total_orders int(11),
  total_revenue float,
  last_order_month int(11)
);
